    }

    public function register(Request $request)
    {
        // Valide les données soumises par l'utilisateur
        $validatedData = $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|unique:users|max:255',
            'password' => 'required|string|min:6|confirmed',
        ]);

        // Crée un nouvel utilisateur avec les informations d'inscription
        $user = User::create([
            'name' => $validatedData['name'],
            'email' => $validatedData['email'],
            'password' => Hash::make($validatedData['password']),
        ]);

        // Connecte automatiquement l'utilisateur après son inscription
        Auth::login($user);

        // Redirige l'utilisateur vers la page de tableau de bord
        return redirect('/dashboard');
    }
        
